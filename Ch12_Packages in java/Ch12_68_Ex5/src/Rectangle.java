package com.shape;

public class Rectangle{
	public int length;
	public int breadth;
	
	public void setLength(int l){
		this.length = l;
	}

	public int getLength(){
		return this.length;
	}

	public void setBreadth(int b){
		this.breadth = b;
	}

	public int getBreadth(){
		return this.breadth;
	}
		
}